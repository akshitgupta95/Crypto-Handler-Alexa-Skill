package com.gmail.akshitgupta1695.cryptoHandler.models;

public class PricesDto {

	private Double dollarPrice;
	private Double euroPrice;
	private Double rupeePrice;
	
	public Double getDollarPrice() {
		return dollarPrice;
	}
	public void setDollarPrice(Double dollarPrice) {
		this.dollarPrice = dollarPrice;
	}
	public Double getEuroPrice() {
		return euroPrice;
	}
	public void setEuroPrice(Double euroPrice) {
		this.euroPrice = euroPrice;
	}
	public Double getRupeePrice() {
		return rupeePrice;
	}
	public void setRupeePrice(Double rupeePrice) {
		this.rupeePrice = rupeePrice;
	}
	
	
}
